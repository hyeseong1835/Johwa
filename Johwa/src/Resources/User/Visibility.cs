
namespace Johwa.Resources.User;

/// <summary>
/// 표시 유형 <br/>
/// Visibility Types
/// </summary>
public enum Visibility
{
    /// <summary>
    /// 사용자 본인을 제외한 모두에게 보이지 않음 <br/>
    /// invisible to everyone except the user themselves
    /// </summary>
    None = 0,

    /// <summary>
    /// 모두에게 보임 <br/>
    /// visible to everyone
    /// </summary>
    Everyone = 1
}